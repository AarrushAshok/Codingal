#Symmetric Difference of two Sets
#In colours
Set1 = {'blue','green'}
Set2 = {'blue','yellow'}
symmetric_difference = Set1.symmetric_difference(Set2)
print("Symmetric Difference:", symmetric_difference)

#In Numbers
Set1 = {1, 2, 3, 4, 5}
Set2 = {1, 5, 6, 7, 8, 9}
symmetric_difference = Set1.symmetric_difference(Set2)
print("Symmetric Difference:", symmetric_difference)